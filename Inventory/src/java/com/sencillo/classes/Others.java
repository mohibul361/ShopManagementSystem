package com.sencillo.classes;

/**
 * 
 * @author Philip
 * This class is used as property of CustomJsonObject
 */

public class Others
{
	private int count;
	private int rowCount;
	private int itemTypeId;
	private String purchaseDate;
	private int warranty;
	private String serialStartFrom;
	
	public int getCount()
	{
		return count;
	}
	public void setCount(int count)
	{
		this.count = count;
	}
	public int getRowCount()
	{
		return rowCount;
	}
	public void setRowCount(int rowCount)
	{
		this.rowCount = rowCount;
	}
	public int getItemTypeId()
	{
		return itemTypeId;
	}
	public void setItemTypeId(int itemTypeId)
	{
		this.itemTypeId = itemTypeId;
	}
	public String getPurchaseDate()
	{
		return purchaseDate;
	}
	public void setPurchaseDate(String purchaseDate)
	{
		this.purchaseDate = purchaseDate;
	}
	public int getWarranty()
	{
		return warranty;
	}
	public void setWarranty(int warranty)
	{
		this.warranty = warranty;
	}
	public String getSerialStartFrom()
	{
		return serialStartFrom;
	}
	public void setSerialStartFrom(String serialStartFrom)
	{
		this.serialStartFrom = serialStartFrom;
	}
	
}
